# operadores logicos

#AND operator
res1 = True & True #devuelve True
res2 = True & False #devuelve False
res3 = False & True #devuelve False
res4 = False & False #devuelve False

#OR operator
res5 = True | True #devuelve True
res6 = True | False #devuelve True
res7 = False | True #devuelve True
res8 = False | False #devuelve False

#NOT operator
res9 = not True #devuelve False da lo contrario
res10 = not False #devuelve True da lo contrario

print(res1, res2, res3, res4, res5,)
print(res6, res7, res8, res9, res10)


